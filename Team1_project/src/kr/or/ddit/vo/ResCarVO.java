package kr.or.ddit.vo;

public class ResCarVO {
	private int res_c_no;
	private String user_id;
	private String car_no;
	private String cnp_code;
	private String car_insr;
	private String rent_start;
	private String rent_end;
	private int rent_price;
	private String res_c_date;
	
	
	public int getRes_c_no() {
		return res_c_no;
	}
	public void setRes_c_no(int res_c_no) {
		this.res_c_no = res_c_no;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getCar_no() {
		return car_no;
	}
	public void setCar_no(String car_no) {
		this.car_no = car_no;
	}
	public String getCnp_code() {
		return cnp_code;
	}
	public void setCnp_code(String cnp_code) {
		this.cnp_code = cnp_code;
	}
	public String getCar_insr() {
		return car_insr;
	}
	public void setCar_insr(String car_insr) {
		this.car_insr = car_insr;
	}
	public String getRent_start() {
		return rent_start;
	}
	public void setRent_start(String rent_start) {
		this.rent_start = rent_start;
	}
	public String getRent_end() {
		return rent_end;
	}
	public void setRent_end(String rent_end) {
		this.rent_end = rent_end;
	}
	public int getRent_price() {
		return rent_price;
	}
	public void setRent_price(int rent_price) {
		this.rent_price = rent_price;
	}
	public String getRes_c_date() {
		return res_c_date;
	}
	public void setRes_c_date(String res_c_date) {
		this.res_c_date = res_c_date;
	}
	
	
}
