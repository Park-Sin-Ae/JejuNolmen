package kr.or.ddit.vo;

public class RoomsVO {
	
	private String lod_code;
	private int room_no;
	private int room_price;
	private String room_type;
	private int room_prs;
	
	private int r_img_no;
	private String r_img_path;
	
	
	public int getR_img_no() {
		return r_img_no;
	}
	public void setR_img_no(int r_img_no) {
		this.r_img_no = r_img_no;
	}
	public String getR_img_path() {
		return r_img_path;
	}
	public void setR_img_path(String r_img_path) {
		this.r_img_path = r_img_path;
	}
	public int getRoom_no() {
		return room_no;
	}
	public void setRoom_no(int room_no) {
		this.room_no = room_no;
	}
	public String getLod_code() {
		return lod_code;
	}
	public void setLod_code(String lod_code) {
		this.lod_code = lod_code;
	}
	public int getRoom_price() {
		return room_price;
	}
	public void setRoom_price(int room_price) {
		this.room_price = room_price;
	}
	public String getRoom_type() {
		return room_type;
	}
	public void setRoom_type(String room_type) {
		this.room_type = room_type;
	}
	public int getRoom_prs() {
		return room_prs;
	}
	public void setRoom_prs(int room_prs) {
		this.room_prs = room_prs;
	}

}
